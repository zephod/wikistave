// Mixin to the application: Vectors object
var App = App || { Vectors: {} };

App.Vectors.Vector = function Vector(source) {
  _.extend(this,source);
  if (!this.path) throw "Error: Vector is missing path";
  this.attr = { 
    stroke: 'none',
    fill: '#000'
  };
  this.translate_x = 0;
  this.translate_y = 0;
  this.scale = 1;
  this.moveTo = function(x, y) { 
    this.translate_x+=x;
    this.translate_y+=y;
    return this.render();
  };
  this.setScale = function(scale) { 
    this.scale = scale;
    return this.render();
  };
  this.render = function() {
    if (this.vector) this.vector.remove();
    this.vector = App.paper.path(this.path);
    this.vector.attr(this.attr);
    this.vector.translate(this.translate_x-this.cx, this.translate_y-this.cy); 
    this.vector.scale(this.scale, this.scale, this.cx, this.cy); 
    return this;
  };
  this.color = function(col) {
    this.attr.fill = col;
    this.render();
    return this;
  };
  this.render();
  return this;
}

